// Read More functionality for Notes section
document.addEventListener('DOMContentLoaded', function() {
    const noteTexts = document.querySelectorAll('.features .read-more-text');
    const maxLength = 200; // Adjust this value as needed
    
    noteTexts.forEach((textEl) => {
        const fullText = textEl.innerHTML.trim();
        
        if (fullText.length > maxLength) {
            // Create a temporary element to strip HTML and get plain text
            const tempDiv = document.createElement('div');
            tempDiv.innerHTML = fullText;
            const plainText = tempDiv.textContent || tempDiv.innerText || '';
            
            if (plainText.length > maxLength) {
                // Find the position to cut while preserving HTML structure
                let shortText = '';
                let charCount = 0;
                let inTag = false;
                let tagContent = '';
                
                for (let i = 0; i < fullText.length; i++) {
                    const char = fullText[i];
                    
                    if (char === '<') {
                        inTag = true;
                        tagContent += char;
                    } else if (char === '>') {
                        inTag = false;
                        tagContent += char;
                        shortText += tagContent;
                        tagContent = '';
                    } else if (inTag) {
                        tagContent += char;
                    } else {
                        if (charCount < maxLength) {
                            shortText += char;
                            charCount++;
                        } else {
                            break;
                        }
                    }
                }
                
                shortText += '...';
                textEl.innerHTML = shortText;
                
                const btn = textEl.nextElementSibling;
                btn.addEventListener('click', function(e) {
                    e.preventDefault();
                    if (btn.textContent.includes('Read More')) {
                        textEl.innerHTML = fullText;
                        btn.textContent = 'Read Less ‚Üí';
                    } else {
                        textEl.innerHTML = shortText;
                        btn.textContent = 'Read More ‚Üí';
                    }
                });
            } else {
                // Hide button if text is not long enough
                textEl.nextElementSibling.style.display = 'none';
            }
        } else {
            // Hide button if text is not long enough
            textEl.nextElementSibling.style.display = 'none';
        }
    });
});
